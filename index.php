			<h1 class="indent" >What Is COVID-19?</h1>
		<p>
			A coronavirus is a kind of common virus that causes an infection in your nose, sinuses, or upper throat. Most coronaviruses aren't dangerous. COVID-19 is a disease that can cause what doctors call a respiratory tract infection. It can affect your upper respiratory tract (sinuses, nose, and throat) or lower respiratory tract (windpipe and lungs). It's caused by a coronavirus named SARS-CoV-2. It spreads the same way other coronaviruses do, mainly through person-to-person contact. Infections range from mild to serious. SARS-CoV-2 is one of seven types of coronavirus, including the ones that cause severe diseases like Middle East respiratory syndrome (MERS) and sudden acute respiratory syndrome (SARS). The other coronaviruses cause most of the colds that affect us during the year but aren’t a serious threat for otherwise healthy people. In early 2020, after a December 2019 outbreak in China, the World Health Organization identified SARS-CoV-2 as a new type of coronavirus. The outbreak quickly spread around the world.
		</p>
		<br>
		<h3 class="indent">Is there more than one strain of SARS-CoV-2?</h3>
		<p>
			It’s normal for a virus to change, or mutate, as it infects people. A Chinese study of 103 COVID-19 cases suggests the virus that causes it has done just that. They found two strains, which they named L and S. The S type is older, but the L type was more common in early stages of the outbreak. They think one may cause more cases of the disease than the other, but they’re still working on what it all means.
		</p>
		<br>
		<h3 class="indent">How long will the coronavirus last?</h3>
		<p>
			It’s too soon to tell how long the pandemic will continue. It depends on many things, including researchers’ work to learn more about the virus, their search for a treatment, and the public’s efforts to slow the spread.
		</p>
		<br>
		<h3 class="indent">Symptoms of COVID-19</h3>
		<p>The main symptoms include:</p>
		<ul>
			<li>Fever</li>
			<li>Coughing</li>
			<li>Shortness of breath</li>
			<li>Fatigue</li>
			<li>Chills, sometimes with shaking</li>
			<li>Body aches</li>
			<li>Headache</li>
			<li>Sore throat</li>
			<li>Loss of smell or taste</li>
			<li>Nausea</li>
			<li>Diarrhea</li>
		</ul>
		<br>
		<p>The virus can lead to pneumonia, respiratory failure, septic shock, and death. Many COVID-19 complications may be caused by a condition known as cytokine release syndrome or a cytokine storm. This is when an infection triggers your immune system to flood your bloodstream with inflammatory proteins called cytokines. They can kill tissue and damage your organs.
		</p>
		<br>
		<h2 class="indent">What to do if you think you have it?</h2>
		<p>If you live in or have traveled to an area where COVID-19 is spreading: If you don’t feel well, stay home. Even if you have mild symptoms like a headache and runny nose, stay in until you’re better. This lets doctors focus on people who are more seriously ill and protects health care workers and people you might meet along the way. You might hear this called self-quarantine.Call the doctor if you have a fever, cough, and trouble breathing. You need to get medical help as soon as possible. Calling ahead (rather than showing up) will let the doctor direct you to the proper place, which may not be your doctor’s office. If you don’t have a regular doctor, call your local board of health. They can tell you where to go for testing and treatment.
		<p>
		Follow your doctor’s advice and keep up with the news on COVID-19. Between your doctor and health care authorities, you’ll get the care you need and information on how to prevent the virus from spreading.
		</p> 
		<br>
		<h4 class="indent">Is COVID-19 worse than the flu?</h4>
		<p>Unlike the flu, nobody is immune to the coronavirus because it’s so new. This means anyone can catch it. The coronavirus also causes higher rates of severe illness and death than the flu. But the symptoms themselves can vary widely from person to person.</p>
		<br>
		<h4 class="indent">Is COVID-19 seasonal like the flu?</h4>
		<p>A few lab studies have found that higher temperatures and humidity levels might help slow the spread of the coronavirus. But experts advise caution and say weather changes won’t matter without thorough public health efforts. Also, past flu pandemics have happened year-round.</p>
		<br>
		<h3 class="indent">Causes of the New Coronavirus</h3>
		<p>Researchers aren’t sure what caused it. There’s more than one coronavirus. They’re common in people and in animals including bats, camels, cats, and cattle. SARS-CoV-2, the virus that causes COVID-19, is similar to MERS and SARS. They all came from bats. Many people who got the disease early on were linked to a large live seafood and animal market in China -- you might hear it called a “wet market.” The first cases may have come from animals sold in the market, then spread from person to person.</p>
		<br>
		<h3 class="indent">Coronavirus Risk Factors</h3>
		<p>Anyone can get COVID-19, and most infections are usually mild, especially in children and young adults. But if you aren’t in an area where COVID-19 is spreading, haven’t traveled from an area where it’s spreading, and haven’t been in contact with someone who has it, your risk of infection is low. People over 65 are most likely to get a serious illness, as are those who live in nursing homes or long-term care facilities, who have weakened immune systems, or who have medical conditions including: High blood pressure, Heart disease, Lung disease, Kidney disease that needs dialysis, Severe obesity, Diabetes, Cancer.</p>
		<br>
		<h2 class="indent">Coronavirus Prevention</h2>
		<ol>
			<li><span>Wash your hands often with soap and water or clean them with an alcohol-based sanitizer.</span> This kills viruses on your hands.</li>
			<li><span>Practice social distancing.</span> Because you can have and spread the virus without knowing it, you should stay home as much as possible. If you do have to go out, stay at least 6 feet away from others.</li>
			<li><span>Cover your nose and mouth in public.</span> If you have COVID-19, you can spread it even if you don’t feel sick. Wear a cloth face covering to protect others. This isn’t a replacement for social distancing. You still need to keep a 6-foot distance between yourself and those around you. Don’t use a face mask meant for health care workers.</li>
			<li><span>Don’t touch your face.</span>Coronaviruses can live on surfaces you touch for several hours. If they get on your hands and you touch your eyes, nose, or mouth, they can get into your body.</li>
			<li><span>Clean and disinfect.</span>You can clean first with soap and water, but disinfect surfaces you touch often, like tables, doorknobs, light switches, toilets, faucets, and sinks. Use a mix of household bleach and water (1/3 cup bleach per gallon of water, or 4 teaspoons bleach per quart of water) or a household cleaner that’s approved to treat SARS-CoV-2. You can check the Environmental Protection Agency (EPA) website to see if yours made the list. Wear gloves when you clean and throw them away when you’re done.</li>
		</ol>
